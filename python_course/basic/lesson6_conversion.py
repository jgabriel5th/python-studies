# Type conversion, typecasting, coercion: it's the act of converting a type to other.
# Immutable and primitives types: str, int, float and bool.
print(1 + 1) # Sum
print('a' + 'b') # Concatenation
# Functions above are using polimorphism by using the same operator '+' for different things.
# print('1' + 1) # It'll raise an error, something usual for programming language with strong typing.

# Type Conversion(practing):
print(int('1'), type(int('1')))
print(int('10') + 14)
print(bool(' '))
print(bool(''))
print(str(11) + 'b')
